    private boolean verifyValuesIfItsNotEmpty() {
        boolean haveEmptyValue = false;
        
        if (txtField.getText().isEmpty()) {
            haveEmptyValue = true;
        }
        return haveEmptyValue;
    }
    
    private void saveBrand(){
    if(!verifyValuesIfItsNotEmpty()){
    Brand brd = new Brand();
    brd.setName((txtField.getText()));
    
    BrandDAO.insert(brd);
    buildTable("");
    jTabbedPane1.setSelectedIndex(1);
    clear();
    }else{
    JOptionPane.showMessageDialog(rootPane, "Algum campo est√° em branco", null, JOptionPane.WARNING_MESSAGE);
    }
        
    }
    
    private void buildTable (String name){
    BrandDAO brdDAO = new BrandDAO ();
    List<Brand> list = brdDAO.getListBrand(name);
    DefaultTableModel tableModel = (DefaultTableModel) tableBrand.getModel();
    tableModel.setRowCount(0);
    
    for(Brand brd : list){
        tableModel.addRow(new Object[]{brd.getId(), brd.getName()});
    }
    }
    
    private void clear (){
    txtField.setText(null);
    txtField1.setText(null);
    }

