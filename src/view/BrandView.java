    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
        int selectedRow = tableBrand.getSelectedRow();
        if (selectedRow >= 0){
            Brand brd = new Brand ();
            brd.setId(Integer.parseInt(tableBrand.getValueAt(selectedRow, 0).toString()));
            BrandDAO.delete(brd);
            buildTable("");
            jTabbedPane1.setSelectedIndex(1);
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um item da tabela!");
        }
    }//GEN-LAST:event_btDeleteActionPerformed

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        int selectedRow = tableBrand.getSelectedRow();
        if (selectedRow >= 0){
            Brand brd = new Brand ();
            brd.setId(Integer.parseInt(tableBrand.getValueAt(selectedRow, 0).toString()));
            txtField.setText(tableBrand.getValueAt(selectedRow, 1).toString());
            txtField1.setText(tableBrand.getValueAt(selectedRow, 0).toString());
            btAtt.setVisible(true);
            btRegister.setVisible(false);
            txtField1.setVisible(true);
            jLabel3.setVisible(true);
            txtField1.setEditable(false);
            jTabbedPane1.setSelectedIndex(0);
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um item da tabela!");
        }
    }//GEN-LAST:event_editButtonActionPerformed

