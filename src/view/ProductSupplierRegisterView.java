    public void buildTable(String name) {
        ProductSupplierDAO productSupplierDao = new ProductSupplierDAO();
        List<ProductSupplier> list = productSupplierDao.getListProductSuppliers(name);
        DefaultTableModel tableModel = (DefaultTableModel) tableProductSuppliers.getModel();
        tableModel.setRowCount(0);

        for (ProductSupplier productSupplier : list) {
            tableModel.addRow(new Object[]{
                productSupplier.getId(),
                productSupplier.getName(),
                productSupplier.getCorporateName(),
                productSupplier.getCnpj(),
                productSupplier.getEmail(),
                productSupplier.getPhoneNumber()
            });
        }
    }


