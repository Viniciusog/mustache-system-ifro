    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (!verifyValuesIfItsNotEmpty()) {
        ProductSupplier productSupplier = new ProductSupplier();
        productSupplier.setId(Integer.parseInt(editId.getText()));
        productSupplier.setCorporateName(editCorporateName.getText());
        productSupplier.setName(editName.getText());
        productSupplier.setPhoneNumber(editTelephoneNumber.getText());
        productSupplier.setCnpj(editCnpj.getText());
        productSupplier.setEmail(editEmail.getText());
        ProductSupplierDAO.update(productSupplier);
        buildTable("");
        jTabbedPane2.setSelectedIndex(1);
        lblID.setVisible(false);
        editId.setVisible(false);
        btnSave.setVisible(true);
        btnUpdate.setVisible(false);
        resetValuesTextField();
        } else {
            alert("Existe campos vazios, verifique e tente novamente!");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

