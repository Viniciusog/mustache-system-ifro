    private void resetValuesTextField() {
        editCorporateName.setText(null);
        editName.setText(null);
        editTelephoneNumber.setText(null);
        editCnpj.setText(null);
        editEmail.setText(null);
        editName.setFocusable(true);
    }

    private void saveService() {
        if (!verifyValuesIfItsNotEmpty()) {
            ProductSupplier productSupplier = new ProductSupplier();
            productSupplier.setCorporateName(editCorporateName.getText());
            productSupplier.setName(editName.getText());
            productSupplier.setPhoneNumber(editTelephoneNumber.getText());
            productSupplier.setCnpj(editCnpj.getText());
            productSupplier.setEmail(editEmail.getText());

            ProductSupplierDAO.insert(productSupplier);
            buildTable("");
            resetValuesTextField();
            jTabbedPane2.setSelectedIndex(1);
        } else {
            alert("Existe um campo vazio!");
        }
    }

    private boolean verifyValuesIfItsNotEmpty() {
        boolean haveEmptyValue = false;

        if (editTelephoneNumber.getText().isEmpty()) {
            haveEmptyValue = true;
        }
        if (editName.getText().isEmpty()) {
            haveEmptyValue = true;
        }
        if (editCnpj.getText().isEmpty()) {
            haveEmptyValue = true;
        }
        if (editCorporateName.getText().isEmpty()) {
            haveEmptyValue = true;
        }
        if (editEmail.getText().isEmpty()) {
            haveEmptyValue = true;
        }
        return haveEmptyValue;
    }

