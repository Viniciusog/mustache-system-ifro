    private boolean verifyValuesIfItsNotEmpty() {
        boolean haveEmptyValue = false;

        if (edId.getText().isEmpty()) {
            haveEmptyValue = true;
        }
        if (edIdClient.getText().isEmpty()) {
            haveEmptyValue = true;
        }
        if (edIdSale.getText().isEmpty()) {
            haveEmptyValue = true;
        }
        if (edDate.getText().isEmpty()) {
            haveEmptyValue = true;
        }
        if (edDueDate.getText().isEmpty()) {
            haveEmptyValue = true;
        }
        if (edTotalValue.getText().isEmpty()) {
            haveEmptyValue = true;
        }
        if (edType.getText().isEmpty()) {
            haveEmptyValue = true;
        }
        if (edValue.getText().isEmpty()) {
            haveEmptyValue = true;
        }
        if (txDescription.getText().isEmpty()) {
            haveEmptyValue = true;
        }        
        return haveEmptyValue;
    }

    private void saveProduct() {
        if (!verifyValuesIfItsNotEmpty()) {
            BillReceive breceive = new BillReceive();
            breceive.setId_bill_receive(Integer.parseInt(edId.getText()));
            breceive.setId_client_bill_receive(Integer.parseInt(edIdClient.getText()));
            breceive.setId_sale_bill_receive(Integer.parseInt(edIdSale.getText()));
            breceive.setDescription_bill_receive(txDescription.getText());
            breceive.setValueBill(Double.parseDouble(edValue.getText()));
            breceive.setValueReceived(Double.parseDouble(edTotalValue.getText()));
            breceive.setDate_emission_bill_receive(edDate.getText());
            breceive.setDue_date_bill_receive(edDueDate.getText());
            breceive.setType_bill_receive(edType.getText());
            if (cbSettled.isSelected()){
                breceive.setSettled_bill_receive(true);
            }else{
                breceive.setSettled_bill_receive(false);
            }
            breceive.setType_bill_receive(edType.getText());
            BillReceiveDAO.insert(breceive);
            buildTable("");
            jTabbedPane1.setSelectedIndex(1);
            clear();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Algum campo está em branco", null, JOptionPane.WARNING_MESSAGE);
        }

    }

    private void buildTable(String name) {
        List<BillReceive> brlist = BillReceiveDAO.getListBillReceive(name);
        DefaultTableModel tableModel = (DefaultTableModel) table.getModel();
        tableModel.setRowCount(0);

        for (BillReceive breceive : brlist) {
            tableModel.addRow(new Object[]{
                breceive.getId_bill_receive(), 
                breceive.getId_client_bill_receive(), 
                breceive.getId_sale_bill_receive(), 
                breceive.getDate_emission_bill_receive(), 
                breceive.getDue_date_bill_receive(), 
                breceive.getValueBill(), 
                breceive.getValueReceived(), 
                breceive.isSettled_bill_receive(), 
                breceive.getType_bill_receive(),
                breceive.getDescription_bill_receive()});
        }

    }

