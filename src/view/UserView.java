    private void tableMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMousePressed
        int selectedRow = table.getSelectedRow();
        if (evt.getClickCount() == 2 && selectedRow != -1) {
            String idUser = table.getValueAt(selectedRow, 0).toString();
            String userName = table.getValueAt(selectedRow, 1).toString();
            boolean accepted = Alert.showOptionDialog("Deseja deletar o usuário: \n " + idUser + " - " + userName, "Confirmação");
            if (accepted) {
                UserDAO.delete(Integer.parseInt(table.getValueAt(selectedRow, 0).toString()));
                buildTable("");
            }
        }
    }//GEN-LAST:event_tableMousePressed

