    private void btCreateUserMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btCreateUserMousePressed
        // TODO add your handling code here:
        if (cbEmployee.getSelectedItem() != null) {
            if (!edNameUser.getText().equals("")) {
                if (!edPasswordUser.getText().equals("")) {
                    User u = new User();
                    u.setName(edNameUser.getText());
                    u.setPassword(edPasswordUser.getText());
                    u.setEmployee((Employee) cbEmployee.getSelectedItem());
                    UserDAO.insert(u);
                } else {
                    Alert.showMessageDialog("Insira uma senha!");
                }
            } else {
                Alert.showMessageDialog("Insira o nome de usuário!");
            }
        } else {
            Alert.showMessageDialog("Selecione um funcionário!");
        }

    }//GEN-LAST:event_btCreateUserMousePressed

