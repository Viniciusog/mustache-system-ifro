    private void btnCloseCashRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCloseCashRegisterActionPerformed
        // TODO add your handling code here:
        if (Alert.showOptionDialog("Deseja encerrar o caixa?!", "Alerta")) {
            CashRegisterDAO.close();
            isCashRegisterOpen();
        }
    }//GEN-LAST:event_btnCloseCashRegisterActionPerformed

    private void btnOpenCashRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOpenCashRegisterActionPerformed
        // TODO add your handling code here:
        if (!txtValue.getText().isEmpty()) {
            CashRegisterModel cashRegister = new CashRegisterModel();
            cashRegister.setInitialValue(Float.parseFloat(txtValue.getText().substring(3).replace(".","").replace(",",".")));
            CashRegisterDAO.open(cashRegister);
            isCashRegisterOpen();
            lblIdCaixa.setText("Caixa: " + String.valueOf(CashRegisterDAO.getIdOpenedCashRegister()));
        }else{
            JOptionPane.showMessageDialog(null, "Erro! O valor incial está vazio. Tente Novamete!");
        }

    }//GEN-LAST:event_btnOpenCashRegisterActionPerformed

