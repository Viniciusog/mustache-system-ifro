    private void isCashRegisterOpen() {
        boolean isOpen = CashRegisterDAO.isOpen();
        System.out.println(isOpen);
        btnCloseCashRegister.setVisible(isOpen);
        btnOpenCashRegister.setVisible(!isOpen);
        txtValue.setEditable(!isOpen);
        labelValueInCashRegister.setVisible(isOpen);
        btnWithdrawMoney.setEnabled(isOpen);
        btnDepositMoney.setEnabled(isOpen);
        if (!isOpen) {
            lblIdCaixa.setText("Caixa: Fechado");
        }else{
             txtValue.setText(String.valueOf(CashRegisterDAO.getValue()));
        }
    }
    
