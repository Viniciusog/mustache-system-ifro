    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int index = tableProduct.getSelectedRow();
        if (index >= 0) {
            Product product = new Product();
            product.setId((int) tableProduct.getValueAt(index, 0));
            product.setBrand((Brand) tableProduct.getValueAt(index, 1));
            product.setName((String) tableProduct.getValueAt(index, 2));
            product.setPrice((Double) tableProduct.getValueAt(index, 3));
            product.setStock((int) tableProduct.getValueAt(index, 4));
            BuyView.addProduct(product);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um item da tabela!", null, JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

