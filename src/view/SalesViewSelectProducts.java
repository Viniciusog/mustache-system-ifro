    private void edNextStepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_edNextStepActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableProduct.getSelectedRow();
        if (selectedRow > -1) {
            Product p = new Product();
            ProductDAO pdao = new ProductDAO();
            p = pdao.getProductById(Integer.parseInt(tableProduct.getValueAt(selectedRow, 0).toString()));

            String qtdResult = JOptionPane.showInputDialog(null, "Insira a quantidade de " + p.getName() + " que será comprada"
                    + "\n Quantidade de " + p.getName() + " no estoque: " + p.getStock(), "");

            try {
                if (!qtdResult.equals("") && !qtdResult.equals(" ")) {
                    if (Integer.parseInt(qtdResult) <= p.getStock() && !qtdResult.equals("") && qtdResult != null) {
                        ProductSale ps = new ProductSale();
                        ps.setProduct(p);
                        ps.setQtdProductSale(Integer.parseInt(qtdResult));
                        SalesView.addProductsSale(ps);

                        /*System.out.println("Nome produto venda: "
                        
                        + sv.getProductSale().getName() + "\n "
                    + "Quantidade produto a ser vendido: " + sv.getQtdProductSale());*/
                        SalesView.addTotalValue(ps.getProduct().getPrice() * ps.getQtdProductSale());
                        SalesView sv = new SalesView(null, rootPaneCheckingEnabled);

                        sv.buildTableProductsSale();
                        sv.buildTableServicesSale();

                        this.dispose();
                        sv.setVisible(true);
                        //sv.setLocationRelativeTo(null);
                    } else {
                        Alert.showWarningMessageDialog("Produto insuficiente no estoque!");
                    }
                }
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }

        } else {
            Alert.showWarningMessageDialog("Selecione um produto da tabela para continuar a venda!");
        }
    }//GEN-LAST:event_edNextStepActionPerformed

